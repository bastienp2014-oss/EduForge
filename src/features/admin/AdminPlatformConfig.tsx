                  {forfait.avantages.map((av, index) => (
                    <div key={index} className="flex items-center gap-1 text-slate-600">
                      <Check size={12} className="text-blue-600 shrink-0" />
                      <span className="truncate">{av}</span>
                    </div>
                  ))}
